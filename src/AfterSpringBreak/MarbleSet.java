package AfterSpringBreak;

public class MarbleSet {

	private String color;
	private int numMarbles;

	public MarbleSet(String xColor, int x) {
		color = xColor;
		numMarbles = x;
	}

	public String getColor() {
		return color;
	}

	public int getNum() {
		return numMarbles;
	}

}
